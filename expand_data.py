import json
import os

def expand_file(file_path, expansion_data):
    if not os.path.exists(file_path):
        print(f"Error: {file_path} not found.")
        return
    with open(file_path, 'r', encoding='utf-8') as f:
        data = json.load(f)
    
    for section in data:
        mot = section['motivation']
        if mot in expansion_data:
            for b in expansion_data[mot]:
                section['brands'].append({
                    "brand_name": b[0],
                    "brand_sov": b[1],
                    "search_index": b[2],
                    "premium_rate": b[3],
                    "products": [{
                        "product_name": b[4],
                        "sales_volume": b[5],
                        "gmv": b[6],
                        "core_value": b[7]
                    }],
                    "topics": [{
                        "topic_name": b[8],
                        "views": b[9],
                        "sentiment_score": b[10]
                    }]
                })
    with open(file_path, 'w', encoding='utf-8') as f:
        json.dump(data, f, ensure_ascii=False, indent=2)
    print(f"Successfully expanded {file_path}")

